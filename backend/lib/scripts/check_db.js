const { PrismaClient } = require('@prisma/client');
const fs = require('fs');
const path = require('path');

const prisma = new PrismaClient();

async function checkDatabase() {
  try {
    console.log('üîç Checking database for word counts by theme...\n');

    // Get all themes with their word counts
    const themesWithCounts = await prisma.theme.findMany({
      include: {
        _count: {
          select: {
            words: true
          }
        }
      },
      orderBy: {
        type: 'asc'
      }
    });

    if (themesWithCounts.length === 0) {
      console.log('‚ùå No themes found in the database');
      return;
    }

    console.log('üìä Word counts by theme:');
    console.log('‚îÄ'.repeat(60));

    let totalWords = 0;
    let totalThemes = themesWithCounts.length;
    
    // Build the report content
    let reportContent = '';
    reportContent += '‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó\n';
    reportContent += '‚ïë           LIAR GAME DATABASE REPORT                       ‚ïë\n';
    reportContent += '‚ïë           Theme Word Count Analysis                       ‚ïë\n';
    reportContent += '‚ï†‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ï£\n';
    reportContent += '‚ïë  Generated: ' + new Date().toLocaleString().padEnd(45) + '‚ïë\n';
    reportContent += '‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù\n\n';
    
    reportContent += '‚îå‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î¨‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î¨‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îê\n';
    reportContent += '‚îÇ Theme Type             ‚îÇ Theme Name              ‚îÇ Words    ‚îÇ\n';
    reportContent += '‚îú‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îº‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îº‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î§\n';

    themesWithCounts.forEach(theme => {
      const wordCount = theme._count.words;
      totalWords += wordCount;
      
      const status = wordCount > 0 ? '‚úÖ' : '‚ö†Ô∏è';
      console.log(`${status} ${theme.type.padEnd(20)} | ${wordCount.toString().padStart(3)} words`);
      
      // Show theme names if available
      if (theme.nameEn) {
        console.log(`   ‚îî‚îÄ ${theme.nameEn}`);
      }
      
      // Add to report
      const themeType = theme.type.padEnd(22);
      const themeName = (theme.nameEn || 'N/A').padEnd(23);
      const wordCountStr = wordCount.toString().padStart(8);
      reportContent += `‚îÇ ${themeType} ‚îÇ ${themeName} ‚îÇ ${wordCountStr} ‚îÇ\n`;
    });
    
    reportContent += '‚îî‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î¥‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚î¥‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îò\n\n';

    console.log('‚îÄ'.repeat(60));
    console.log(`üìà Summary:`);
    console.log(`   Total themes: ${totalThemes}`);
    console.log(`   Total words: ${totalWords}`);
    console.log(`   Average words per theme: ${totalThemes > 0 ? (totalWords / totalThemes).toFixed(1) : 0}`);
    
    // Add summary to report
    reportContent += '‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n';
    reportContent += '                        SUMMARY                            \n';
    reportContent += '‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n';
    reportContent += `Total Themes:           ${totalThemes}\n`;
    reportContent += `Total Words:            ${totalWords}\n`;
    reportContent += `Average Words/Theme:    ${totalThemes > 0 ? (totalWords / totalThemes).toFixed(1) : 0}\n`;
    reportContent += '\n';

    // Show themes with no words
    const emptyThemes = themesWithCounts.filter(theme => theme._count.words === 0);
    if (emptyThemes.length > 0) {
      console.log(`\n‚ö†Ô∏è  Themes with no words:`);
      reportContent += '‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ\n';
      reportContent += '                   THEMES WITH NO WORDS                    \n';
      reportContent += '‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ\n';
      emptyThemes.forEach(theme => {
        console.log(`   - ${theme.type} (${theme.nameEn || 'No English name'})`);
        reportContent += `‚Ä¢ ${theme.type} (${theme.nameEn || 'No English name'})\n`;
      });
      reportContent += '\n';
    }

    // Show themes with most words
    const topThemes = themesWithCounts
      .filter(theme => theme._count.words > 0)
      .sort((a, b) => b._count.words - a._count.words)
      .slice(0, 3);

    if (topThemes.length > 0) {
      console.log(`\nüèÜ Top themes by word count:`);
      reportContent += '‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ\n';
      reportContent += '                  TOP THEMES BY WORD COUNT                 \n';
      reportContent += '‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ\n';
      topThemes.forEach((theme, index) => {
        const medal = index === 0 ? 'ü•á' : index === 1 ? 'ü•à' : 'ü•â';
        const position = index === 0 ? '1st' : index === 1 ? '2nd' : '3rd';
        console.log(`   ${medal} ${theme.type} (${theme.nameEn || 'No English name'}): ${theme._count.words} words`);
        reportContent += `${position}: ${theme.type} (${theme.nameEn || 'No English name'}): ${theme._count.words} words\n`;
      });
    }
    
    // Save report to file
    const reportsDir = path.join(__dirname, 'reports');
    if (!fs.existsSync(reportsDir)) {
      fs.mkdirSync(reportsDir, { recursive: true });
    }
    
    const timestamp = new Date().toISOString().replace(/:/g, '-').split('.')[0];
    const fileName = `theme_word_count_${timestamp}.txt`;
    const filePath = path.join(reportsDir, fileName);
    
    fs.writeFileSync(filePath, reportContent, 'utf8');
    console.log(`\nüíæ Report saved to: ${filePath}`);

  } catch (error) {
    console.error('‚ùå Error checking database:', error);
  } finally {
    await prisma.$disconnect();
  }
}

// Run the check
checkDatabase();
